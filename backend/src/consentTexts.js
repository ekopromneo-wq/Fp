/**
 * Возвращает снимок текста по типу согласия. Бросает на неизвестный тип, чтобы
 * не записать в журнал пустое «доказательство».
 */
export function consentTextSnapshot(consentType) {
  const text = CONSENT_TEXTS[consentType];

  if (!text) {
    throw new Error(`Unknown consent type: ${consentType}`);
  }

  return { textVersion: CONSENT_TEXT_VERSION, textSnapshot: text };
}
